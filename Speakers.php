                            <h4> Toothpick: a fresh approach to Dependency Injection on Android  </h4>
                            
                            
                            <div class="bio-content">
                                <p> 
                                      Daniel is an Android Software Engineer at Groupon. He is a passionate Science geek with special interest in performance, design and security.<br />

Before joining Groupon, he worked as a Web and Android developer for 6 years, participating in the development of some Android applications such as Hostelworld, Bonobu and now Groupon.<br />

He is keen on contributing to the community by writing articles and collaborating on some open source projects: Toothpick, Dart & Henson, etc. Moreover, he has taken part in some conferences: Berlin Droidcon 2016, Mobile Era 2016 or AndDevCon San Francisco 2016.


                                </p>
                            </div>
                            
                            <div class="bio-stoyes">
                                <p> 
                                  


You find RoboGuice simple but slow? And you think Dagger 1 & 2 are fast but complex and bloated? Toothpick is the best of both worlds!
<br />
Toothpick is a scope tree based, runtime but reflection free implementation of JSR 330. It is pure Java, with a special focus on Android. Toothpick is fast (even faster than Dagger 2 in some cases!) and is simpler to use, with less boilerplate code. Its syntax is very close to Guice. It supports named dependencies, lazy dependencies, providers, and has built-in support for custom scopes. As compared to Dagger 2, Toothpick provides more powerful testing support, allowing you to leverage DI to mock dependencies using either mockito or easymock. Its explicit scope tree helps developers to build more robust apps. It provides more fine grained control to easily implement complex user flows that span multiple activities or state preservation across configuration changes (e.g. rotations), a common issue when implementing the MVP pattern.<br /> 

During this talk for experts, we will introduce Toothpick, its main features and how it compares to other DI libs. We will explain how Toothpick scopes and scope annotations can solve advanced use cases when developing Android apps.

